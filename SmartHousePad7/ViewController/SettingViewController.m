#pragma mark - Table view delegate

- (void)tableView:(UITableView *)tableView didSelectRowAtIndexPath:(NSIndexPath *)indexPath
{
    switch (indexPath.row) {
        case UrlSettingRow:
            [self pushControlUrlSettingViewController];
            break;
            
        default:
            break;
    }
}

#pragma mark - private messages
-(void)pushControlUrlSettingViewController{
    ControlUrlSettingViewController*cusvc=[[ControlUrlSettingViewController alloc]init];
    [self.navigationController pushViewController:cusvc animated:YES];
    [cusvc release];
}

